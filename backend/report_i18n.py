"""Server-side i18n for the deterministic $25 feasibility report (no LLM).

The report path is intentionally LLM-free (see ``report_render.py`` and the
2026-06-16 OOM/reranker incident). Translation is therefore a static catalog:
``make_translator(lang)`` returns a ``t(key, **kwargs)`` callable used both as a
Jinja global in ``templates/zoning_report.html`` and inside the narrative
builders in ``main.py``.

Design notes:
* English values are byte-identical copies of the original template literals, so
  the English report (and every existing test that asserts on its text) is
  unchanged: ``t("k")`` for ``lang="en"`` returns exactly the old string.
* The Jinja environment has ``autoescape=True``. Catalog values therefore hold
  PLAIN TEXT only — never HTML tags (they would be escaped). A literal ``&`` in a
  value renders correctly (autoescape emits ``&amp;``). HTML structure stays in
  the template.
* Numbers keep the existing ``{:,}`` grouping — Spanish (es-MX, matching the
  frontend ``LOCALE_MAP``) uses the same thousands/decimal convention as en-US,
  so only dates need localizing. Currency stays USD.
"""
from __future__ import annotations

from typing import Callable

SUPPORTED = ("en", "es")
DEFAULT_LANG = "en"

# Spanish month names — avoids pulling in Babel just for one date string.
_ES_MONTHS = {
    1: "enero", 2: "febrero", 3: "marzo", 4: "abril", 5: "mayo", 6: "junio",
    7: "julio", 8: "agosto", 9: "septiembre", 10: "octubre", 11: "noviembre",
    12: "diciembre",
}


def normalize_lang(language: str | None) -> str:
    """Map an incoming language tag (``es``, ``es-MX`` …) to a supported code."""
    if not language:
        return DEFAULT_LANG
    code = language.split("-")[0].strip().lower()
    return code if code in SUPPORTED else DEFAULT_LANG


def format_report_date(d, language: str | None) -> str:
    """Localized long-form report date. en: ``June 20, 2026``; es: ``20 de junio de 2026``."""
    lang = normalize_lang(language)
    if lang == "es":
        return f"{d.day} de {_ES_MONTHS[d.month]} de {d.year}"
    return d.strftime("%B %d, %Y")



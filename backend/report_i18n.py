MESSAGES: dict[str, dict[str, str]] = {"en": _EN, "es": _ES}


def make_translator(language: str | None) -> Callable[..., str]:
    """Return ``t(key, **kwargs)`` for the language, falling back to en then key."""
    lang = normalize_lang(language)
    primary = MESSAGES.get(lang, _EN)

    def t(key: str, **kwargs) -> str:
        template = primary.get(key)
        if template is None:
            template = _EN.get(key, key)
        if kwargs:
            try:
                return template.format(**kwargs)
            except (KeyError, IndexError, ValueError):
                return template
        return template

    return t


def make_plural(language: str | None) -> Callable[..., str]:
    """Return ``tn(key, n, **kwargs)`` selecting the ``__one``/``__other`` variant.

    Falls back to the bare ``key`` when no plural variants are defined, so a key
    used with both ``t`` and ``tn`` still resolves.
    """
    lang = normalize_lang(language)
    primary = MESSAGES.get(lang, _EN)

    def tn(key: str, count: int, **kwargs) -> str:
        suffix = "__one" if count == 1 else "__other"
        template = primary.get(key + suffix) or _EN.get(key + suffix)
        if template is None:
            template = primary.get(key) or _EN.get(key, key)
        kwargs.setdefault("n", count)
        try:
            return template.format(**kwargs)
        except (KeyError, IndexError, ValueError):
            return template

    return tn
